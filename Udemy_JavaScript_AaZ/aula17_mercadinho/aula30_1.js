let dia = 80

switch(dia) {
    case 1:
        console.log('hoje é segunda.')
        break
    case 2:
        console.log('hoje é terça.')
        break
    case 3:
        console.log('hoje é quarta.')
        break
    case 4:
        console.log('hoje é quinta.')
        break
    default:
        console.log('número inválido, tente novamente!')
        break;
}